    <h2><ins>Hombros</ins></h2>
    <dl>
        <dt><b><ins>Press Militar (con mancuernas o barra)</ins></b></dt>
        <dd>El press militar es el más común de los ejercicios de hombros 
            realizados en gimnasios y el movimiento final del levantamiento en 
            halterofilia.</dd>
        <dd><i>Trabaja:</i> El deltoides anterior y externo, aunque también pone a trabajar
            al serrato mayor y al trapecio, estos últimos actuando como 
            músculos antagonistas en el ejercicio.</dd>
        <dt><b><ins>Press tras Nuca</ins></b></dt>
        <dd><i>Trabaja:</i> Deltoides sobre todo el deltoides medio y posterior, 
            Tríceps braquial,
            Serrato anterior,
            Trapecio</dd>
        <dt><b><ins>Elevaciones Laterales</ins></b></dt>
        <dd>El ejercicio de elevaciones laterales son probablemente el mejor ejercicio 
            que tenemos a nuestra disposición para entrenar el deltoides lateral, 
            ya que es el movimiento primordial del cual se encarga esta 
            porción del deltoides, es decir, abducir el brazo, 
            separarlo del cuerpo. </dd>
        <dd><i>Trabaja:</i> principalmente el deltoides medio y colocando las manos en 
            diferente posiciones de partida, se puede trabajar el conjunto de sus haces 
            que permiten desplazar el brazo hacia todos los planos. También se trabaja 
            con este movimiento el trapecio y el haz anterior del deltoides, 
            por lo que resulta un buen ejercicio para el hombro y de manera secundaria 
            para el cuello.</dd>
    </dl>

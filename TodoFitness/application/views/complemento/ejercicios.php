    <h2><ins>Piernas</ins></h2>
    <dl>
        <dt><b><ins>Sentadillas libres (con barra)</ins></b></dt>
        <dd>Las sentadillas son un ejercicio básico que exige esfuerzo cardiovascular.</dd>
        <dd><i>Trabaja:</i> Durante el movimiento hacia abajo y posteriormente arriba, 
            se trabajan los siguientes grupos musculares:
            Muslos, Glúteos, Extensores y parte baja de la espalda, Abdomen.
            De igual manera, en el descenso las rodillas, cadera y tobillos 
            toman acción en las sentadillas.</dd>
        <dt><b><ins>Prensa</ins></b></dt>
        <dd>La prensa de piernas es uno de los ejercicios de piernas más efectivos y conocidos 
            en un dispositivo de fuerza, y se puede realizar en casi todos los gimnasios.</dd>
        <dd><i>Trabaja:</i> Pierna y glúteos, más específicamente, 
            el glúteo mayor, el cuádriceps femoral y el bíceps femoral.</dd>
        <dt><b><ins>Extensión de Cuádriceps en Máquina</ins></b></dt>
        <dd>El ejercicio extensión de cuádriceps es uno de los mejores que podremos realizar 
            para completar nuestro trabajo de pierna. En concreto, es perfecto para focalizar un 
            trabajo en nuestro cuádriceps y aislarlo al máximo para conseguir una mayor hipertrofia.</dd>
        <dd><i>Trabaja:</i> Es un ejercicio de aislamiento y por tanto solo involucra 
            el cuádriceps (la parte delantera del muslo). Pero aunque solo involucre un músculo, 
            el cuádriceps es uno de los músculos más potentes de nuestro cuerpo, y su nombre se 
            debe a que tiene 4 cabezas. Por tanto con las extensiones de cuádriceps podemos focalizar 
            más en una cabeza u otra del músculo dependiendo de la posición de nuestros pies.</dd>
        <dt><b><ins>Curl Femoral</ins></b></dt>
        <dd>Este movimiento de flexión de piernas es probablemente uno de los ejercicios más 
            importantes para los practicantes de entrenamiento de fuerza que desean 
            desarrollar la masa y la definición muscular de la parte posterior del muslo. 
            El curl fermoral aumenta la estabilidad de la articulación de la rodilla, que es un 
            factor clave en la prevención de lesiones.</dd>
        <dd><i>Trabaja:</i> El grupo muscular de los isquiotibiales se solicita durante 
            la curvatura de la pierna. Este grupo está formado por el músculo semimembranoso, 
            semitendinoso y bíceps femoral. Estos músculos biarticulares actúan tanto en las 
            articulaciones de la cadera como de la rodilla. Los gemelos gemelos se estresan si doblas 
            los tobillos durante la ejecución.</dd>
    </dl>
